#!/usr/bin/env python3
"""

Module: 0-add
Defines a function to add two floats.
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers and return their sum.

    Args:
    a (float): The first float number.
    b (float): The second float number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
